'''
Задание 1: Работа с списками и срезами

Создайте список из 10 элементов с разными типами 
данных (строки, числа и булевы значения) и выведите его на экран.

Используя срезы, выведите на экран первые 5 элементов списка, 
последние 3 элемента списка и каждый второй элемент списка.

Измените 3 элемент списка на новое значение и выведите измененный список на экран.
'''

# Создание списка из 10 элементов
my_list = ['apple', 123, True, 'banana', 45, False, 7.89, 'orange', 999, True]

# Вывод списка на экран
print("Список из 10 элементов:", my_list)

# Используя срезы, выводим первые 5 элементов списка
print("Первые 5 элементов списка:", my_list[:5])

# Используя срезы, выводим последние 3 элемента списка
print("Последние 3 элемента списка:", my_list[-3:])

# Используя срезы, выводим каждый второй элемент списка
print("Каждый второй элемент списка:", my_list[::2])

# Изменение третьего элемента списка на новое значение
my_list[2] = 'grape'

# Вывод измененного списка на экран
print("Измененный список:", my_list)

'''
Задание 2: Работа с условиями и циклами

Попросите пользователя ввести число с клавиатуры. 
Если число делится на 3 без остатка, выведите сообщение "Число делится на 3". 
Если число больше 10, выведите сообщение "Число больше 10". 
Если число не удовлетворяет ни одному из условий, 
выведите сообщение "Число не соответствует условиям".
'''

# Просим пользователя ввести число
number = int(input("Введите число: "))

# Проверяем условия
if number % 3 == 0:
    print("Число делится на 3")
elif number > 10:
    print("Число больше 10")
else:
    print("Число не соответствует условиям")
